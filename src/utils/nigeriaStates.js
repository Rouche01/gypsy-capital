export const numberWithCommas = (x) => {
  x = x?.toFixed(2);
  x = x?.toString().split(".");
  if (x) {
    return `${x[0].replace(/\B(?=(\d{3})+(?!\d))/g, ",")}.${x[1]}`;
  }
  return null;
};
